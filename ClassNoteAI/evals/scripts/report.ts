/**
 * Aggregate the per-metric reports (asr-wer, rag-mrr, summary-judge)
 * into one daily `reports/YYYY-MM-DD.md` that's cheap to scan and
 * commits cleanly into git so drift shows up in blame.
 *
 * Kept deliberately simple: the per-metric scripts produce standalone
 * markdown, this one concatenates + prepends a summary header. When
 * the metric pipelines get richer (thresholds, trend graphs, etc.),
 * this is the right place to compute deltas vs the previous day's
 * report.
 */

import { readdir, readFile, writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const reportsDir = join(here, '..', 'reports');

async function main() {
    const date = new Date().toISOString().slice(0, 10);
    await mkdir(reportsDir, { recursive: true });

    const allFiles = await readdir(reportsDir).catch(() => [] as string[]);
    const todays = allFiles.filter(
        (f) => f.endsWith(`-${date}.md`) && f !== `${date}.md`,
    );

    const parts: string[] = [
        `# ClassNoteAI Quality Report — ${date}`,
        '',
        'Generated by the nightly eval harness. See evals/README.md for',
        'methodology + how to add fixtures. Any single number is less',
        'interesting than the trend — open git blame on this file.',
        '',
    ];

    if (todays.length === 0) {
        parts.push(
            '> No per-metric reports were produced today. Either the',
            '> fixtures are empty or the individual eval scripts failed',
            '> — check the workflow logs.',
        );
    } else {
        for (const f of todays.sort()) {
            const body = await readFile(join(reportsDir, f), 'utf-8');
            parts.push(body, '');
        }
    }

    const out = join(reportsDir, `${date}.md`);
    await writeFile(out, parts.join('\n'));
    console.log(`[eval:report] wrote aggregate: reports/${date}.md`);
}

main().catch((err) => {
    console.error('[eval:report] fatal:', err);
    process.exit(1);
});
